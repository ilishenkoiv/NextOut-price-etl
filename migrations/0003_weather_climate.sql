  iata       text        not null,
  month      smallint    not null check (month between 1 and 12),
  avg_tmax   numeric(4,1) not null,   -- °C, 30-year mean of daily Tmax
  rain_days  numeric(4,1) not null,   -- mean days/month with ≥ 1 mm precipitation
